import type { OpenAPIDocument, OperationInfo, SpecSummary } from '../../shared/openapi.js';
import { extractOperations } from '../../shared/openapi.js';
import { operationToMethodName, toPascalCase, toSnakeCase } from '../../shared/naming.js';

interface SchemaObject {
  type?: string;
  format?: string;
  description?: string;
  properties?: Record<string, SchemaObject>;
  required?: string[];
  items?: SchemaObject;
  enum?: unknown[];
  allOf?: SchemaObject[];
  oneOf?: SchemaObject[];
  anyOf?: SchemaObject[];
  nullable?: boolean;
  $ref?: string;
  additionalProperties?: boolean | SchemaObject;
}

interface FieldDefinition {
  identifier: string;
  type: string;
  required: boolean;
  description?: string;
}

interface TypedDictShape {
  bases: string[];
  requiredFields: FieldDefinition[];
  optionalFields: FieldDefinition[];
}

interface MethodParam {
  identifier: string;
  type: string;
  required: boolean;
}

interface MethodShape {
  methodName: string;
  signature: string[];
  pathExpr: string;
  queryExpr?: string;
  bodyArg?: string;
  returnType: string;
  description: string;
}

